package com.example.worldexplorer.model;

public class country {

    private String mCountryCode;
    private String mCountryName;
    private String mContinentCode;
    private String mContinentName;
    private String mCurrencyCode;
    private String mDialingCode;

    public String getCountryCode() {
        return mCountryCode;
    }

    public void setCountryCode(String mCountryCode) {
        this.mCountryCode = mCountryCode;
    }

    public String getCountryName() {
        return mCountryName;
    }

    public void setCountryName(String mCountryName) {
        this.mCountryName = mCountryName;
    }

    public String getContinentCode() {
        return mContinentCode;
    }

    public void setContinentCode(String mContinentCode) {
        this.mContinentCode = mContinentCode;
    }

    public String getContinentName() {
        return mContinentName;
    }

    public void setContinentName(String mContinentName) {
        this.mContinentName = mContinentName;
    }

    public String getCurrencyCode() {
        return mCurrencyCode;
    }

    public void setCurrencyCode(String mCurrencyCode) {
        this.mCurrencyCode = mCurrencyCode;
    }

    public String getDialingCode() {
        return mDialingCode;
    }

    public void setDialingCode(String mDialingCode) {
        this.mDialingCode = mDialingCode;
    }

}
